    private void addMarkerButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addMarkerButtonActionPerformed
        try {
            double longitudeVal = Double.parseDouble(longitude.getText());
            double latitudeVal = Double.parseDouble(latitude.getText());
            double altitudeVal = Double.parseDouble(altitude.getText());
            int coordinateId = findNewCoordinateId();
            this.pathPlan.addCoordinate(new Coordinate(coordinateId, latitudeVal, longitudeVal, altitudeVal));
            updateCoordinateDisplayArea();
            longitude.setText("");
            latitude.setText("");
            altitude.setText("");
        } catch (Exception ex) {
            ex.printStackTrace();
            messageBox1.setText(SystemConstants.INVALID_COORDINATES);
        }
    }//GEN-LAST:event_addMarkerButtonActionPerformed

    private void okButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_okButtonActionPerformed
        try {
            String planName = this.planName.getText().trim();
            double uavSpeed = Double.parseDouble(this.uavSpeed.getText().trim());
            String fileSavePath = this.savePath.getText().trim();

            if ("".equals(planName) || "".equals(fileSavePath)) {
                this.messageBox2.setText(SystemConstants.REQUIRED);
            } else {
                this.pathPlan.setFilePath(fileSavePath);
                this.pathPlan.setSpeed(uavSpeed);
                this.pathPlan.setName(planName);
                pathPlan.setIcon(new ImageIcon(SystemConstants.PATH_TO_MODEL_ICON));
                this.dispose();
            }

        } catch (Exception ex) {
            this.messageBox2.setText(SystemConstants.INVALID_UAV_SPEED);
        }
    }//GEN-LAST:event_okButtonActionPerformed

