    public PathPlan showDialog(PathPlan pathPlan) {
        if (pathPlan == null) {
            this.pathPlan = new PathPlan();
            this.pathPlan.setCoordinateList(new ArrayList<>());
        } else {
            this.pathPlan = pathPlan;
            updateCoordinateDisplayArea();
        }
        this.setLocationRelativeTo(null);
        this.setVisible(true);
        return this.pathPlan;
    }

