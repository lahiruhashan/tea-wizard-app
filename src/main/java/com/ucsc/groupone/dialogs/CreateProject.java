    private void createLogDirectory(String absolutePath) {
        File logDir = new File(absolutePath + "/logs");
        if (logDir.mkdir()) {
            WizardLogger.log("log directory created", Level.INFO, null);
        } else {
            WizardLogger.log("log directory creation failed", Level.INFO, null);
        }
    }
