    private void browseButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_browseButtonActionPerformed
        JFileChooser fileChooser = new JFileChooser(new File(this.userHome
                .concat("/").concat(SystemConstants.IDE_FOLDER)));
        fileChooser.setFileSelectionMode(JFileChooser.DIRECTORIES_ONLY);
        fileChooser.showOpenDialog(this);
        projectLocationField.setText(fileChooser.getSelectedFile().getAbsolutePath());
    }//GEN-LAST:event_browseButtonActionPerformed

    private void cancelButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cancelButtonActionPerformed
        this.dispose();
        returnValue = SystemConstants.RETURN_CANCEL;
    }//GEN-LAST:event_cancelButtonActionPerformed

    private void createButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_createButtonActionPerformed

        File projectFolder = new File(projectFolderField.getText());
        if (!projectFolder.exists()) {
            boolean folderCreated = projectFolder.mkdirs();
            if (folderCreated) {
                String absolutePath = projectFolder.getAbsolutePath();
                SystemVariables.setProjectRootFolder(absolutePath);
                createLogDirectory(absolutePath);
                ProjectXMLFileCreator.createProjectFile(projectNameField.getText(), absolutePath);
                this.returnValue = SystemConstants.RETURN_SUCCESS;
            } else {
                this.returnValue = SystemConstants.RETURN_ERROR;
            }
            this.dispose();
        } else {
            messageBoxLabel.setText(SystemConstants.PROJECT_EXIST_NOTE);
            projectNameField.setBorder(BorderFactory.createBevelBorder(1, Color.RED, Color.RED));
        }
    }//GEN-LAST:event_createButtonActionPerformed

    private void projectNameFieldMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_projectNameFieldMouseClicked
        if (!messageBoxLabel.getText().equals("")) {
            messageBoxLabel.setText("");
            projectNameField.setBorder(BorderFactory.createBevelBorder(1));
        }
    }//GEN-LAST:event_projectNameFieldMouseClicked

