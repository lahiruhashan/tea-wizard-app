    private void predictClassifierButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_predictClassifierButtonActionPerformed

        String videoPath = getTestVideoFolderPath();
        if (videoPath == null) {
            WizardLogger.log("Retreiving Testing Video File Failed", Level.INFO, logArea);
            return;
        }

        WizardLogger.log("Please Wait Until The Process Is Finished", Level.INFO, logArea);
        new Thread(() -> {
            try {
                running = true;
                WizardLogger.log("Prediction Running...", Level.INFO, logArea);
                HttpClient client = HttpClientBuilder.create().build();
                ObjectMapper mapper = new ObjectMapper();
                ClassifierModelDTO jsonModel = new ClassifierModelDTO();
                jsonModel.setFrozen_graph(modelIcon.getFigPath());
                jsonModel.setLabels_file(modelIcon.getCfPath());
                jsonModel.setTest_video_file(videoPath);
                jsonModel.setNum_of_classes(modelIcon.getNumberOfClasses());
                String json = mapper.writeValueAsString(jsonModel);
                HttpPost request = new HttpPost(SystemConstants.BASE_URL + "/predict/video");
                StringEntity entity = new StringEntity(json);
                request.setEntity(entity);
                request.setHeader("Content-type", "application/json");
                HttpResponse response = client.execute(request);
                HttpEntity httpEntity = response.getEntity();
                String output = EntityUtils.toString(httpEntity);
                System.out.println(output);
                running = false;
                WizardLogger.log("Prediction Completed", Level.INFO, logArea);
            } catch (IOException ex) {
                Logger.getLogger(StartFrame.class.getName()).log(Level.SEVERE, null, ex);
            }
        }).start();
    }//GEN-LAST:event_predictClassifierButtonActionPerformed

